const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const attendanceValidation = require('../../validations/attendance.validation');
const attendanceController = require('../../controllers/attendance.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageUsers'), validate(attendanceValidation.createAttendance), attendanceController.createAttendance)
  .get(auth('getUsers'), validate(attendanceValidation.getAttendances), attendanceController.getAttendances);

router
  .route('/:attendanceId')
  .get(auth('getUsers'), validate(attendanceValidation.getAttendance), attendanceController.getAttendance)
  .patch(auth('manageUsers'), validate(attendanceValidation.updateAttendance), attendanceController.updateAttendance)
  .delete(auth('manageUsers'), validate(attendanceValidation.deleteAttendance), attendanceController.deleteAttendance);

module.exports = router;

