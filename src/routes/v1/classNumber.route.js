const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const classNumberController = require('../../controllers/classNumber.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageUsers'), classNumberController.createClassNumber)
  .get(auth('getUsers'), classNumberController.getClassNumbers);

router
  .route('/:id')
  .get(auth('getUsers'), classNumberController.getClassNumber)
  .patch(auth('manageUsers'), classNumberController.updateClassNumber)
  .delete(auth('manageUsers'), classNumberController.deleteClassNumber);

module.exports = router;

