const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const examValidation = require('../../validations/exam.validation');
const examController = require('../../controllers/exam.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageUsers'), validate(examValidation.createExam), examController.createExam)
  .get(auth('getUsers'), validate(examValidation.getExams), examController.getExams);

router
  .route('/:id')
  .get(auth('getUsers'), validate(examValidation.getExam), examController.getExam)
  .patch(auth('manageUsers'), validate(examValidation.updateExam), examController.updateExam)
  .delete(auth('manageUsers'), validate(examValidation.deleteExam), examController.deleteExam);

module.exports = router;

