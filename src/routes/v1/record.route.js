const express = require('express');
const auth = require('../../middlewares/auth');
const recordController = require('../../controllers/record.controller');

const router = express.Router();

router.route('/student-creation-form').get(auth('getUsers'), recordController.getRecordsForStudentForm);

router.route('/teacher-creation-form').get(auth('getUsers'), recordController.getRecordsForTeacherForm);

router.route('/parent-creation-form').get(auth('getUsers'), recordController.getRecordsForParentForm);

router.route('/batch-creation-form').get(auth('getUsers'), recordController.getRecordsForBatchForm);

router.route('/attendance-creation-form').get(auth('getUsers'), recordController.getRecordsForAttendanceForm);

router.route('/payment-creation-form').get(auth('getUsers'), recordController.getRecordsForPaymentForm);

router.route('/exam-creation-form').get(auth('getUsers'), recordController.getRecordsForExamForm);

router.route('/parent-for-mobile/:id').get(auth('getUsers'), recordController.getRecordsForParentForMobile);

router.route('/student-for-mobile/:id').get(auth('getUsers'), recordController.getRecordsForStudentForMobile);

router.route('/student-attendance-for-mobile/:id').get(recordController.getRecordsForStudentAttendanceForMobile);

module.exports = router;

