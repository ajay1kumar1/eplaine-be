const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const batchValidation = require('../../validations/batch.validation');
const batchController = require('../../controllers/batch.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageUsers'), validate(batchValidation.createBatch), batchController.createBatch)
  .get(auth('getUsers'), validate(batchValidation.getBatches), batchController.getBatches);

router
  .route('/:batchId')
  .get(auth('getUsers'), validate(batchValidation.getBatch), batchController.getBatch)
  .patch(auth('manageUsers'), validate(batchValidation.updateBatch), batchController.updateBatch)
  .delete(auth('manageUsers'), validate(batchValidation.deleteBatch), batchController.deleteBatch);

module.exports = router;

