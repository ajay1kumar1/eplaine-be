const express = require('express');
const auth = require('../../middlewares/auth');
const examAttemptController = require('../../controllers/examAttempt.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageUsers'), examAttemptController.createExamAttempt)
  .get(auth('getUsers'), examAttemptController.getExamAttempts);

router
  .route('/:id')
  .get(auth('getUsers'), examAttemptController.getExamAttempt)
  .patch(auth('manageUsers'), examAttemptController.updateExamAttempt)
  .delete(auth('manageUsers'), examAttemptController.deleteExamAttempt);

module.exports = router;

